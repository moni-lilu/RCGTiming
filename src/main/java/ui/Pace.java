package ui;

public class Pace {
    private double l;
    private double t;
    private double c;
    private double s;

    public double getL() {
        return l;
    }

    public double getT() {
        return t;
    }

    public double getC() {
        return c;
    }

    public double getS() {
        return s;
    }

    public void setL(double l) {
        this.l = l;
    }

    public void setT(double t) {
        this.t = t;
    }

    public void setC(double c) {
        this.c = c;
    }

    public void setS(double s) {
        this.s = s;
    }


}
